public class Boss {
    private int health;
    private int damage;
    private int kinetic;

    public int getHealth(){
        return this.health;
    }
    public int getDamage(){
        return this.damage;
    }
    public int getKinetic (){
        return this.kinetic;
    }
    public void setHealth(int health){
        this.health=health;
    }
    public void setDamage(int damage){
        this.damage=damage;
    }
    public void setKinetic(int kinetic){
        this.kinetic=kinetic;
    }
}
